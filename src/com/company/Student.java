package com.company;

public class Student {

    String name;

    public Student(String name) {
        this.name = name;
    }

    int theMostExpensiveStudent;
    int accurate;
    int punctual;
    int TheCheapest;

    public Student(String name, int theMostExpensiveStudent, int accurate, int punctual, int theCheapest) {
        this.name = name;
        this.theMostExpensiveStudent = theMostExpensiveStudent;
        this.accurate = accurate;
        this.punctual = punctual;
        TheCheapest = theCheapest;
    }



}
